package Anand.String_v1_v2_v3;

public class Test03_DiffWays {
	public static void main(String[] args) {
		char[] ch = { 'a', 'n', 'd' };
		System.out.println(ch);

		// string
		String s = "and";
		System.out.println("String:" + s);

		// StringBuffer
		StringBuffer s1 = new StringBuffer("and");
		System.out.println("StringBuffer:" + s1);

		// StringBuilder
		StringBuilder s2 = new StringBuilder("and");
		System.out.println("StringBuilder:" + s2);
	}

}
