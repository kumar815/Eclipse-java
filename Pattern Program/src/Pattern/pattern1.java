package Pattern;

public class pattern1{
	public static void main(String[] args) {
		int rows = 10;
		for(int i=1;i<=rows;++i) {
		//inner for loop colums)
			for(int j=1;j<=i;++j){
				System.out.print(j-10 +" ");
			}
			System.out.println(" ");
		}
	}
}


